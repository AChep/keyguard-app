private fun String.isKnownTextAttachmentPreviewFileName(): Boolean =
    this == ".env" ||
        startsWith(".env.") ||
        this == ".editorconfig" ||
        this == ".gitattributes" ||
        this == ".gitignore" ||
        this == ".dockerignore" ||
        this == "dockerfile" ||
        startsWith("dockerfile.") ||
        this == "containerfile" ||
        startsWith("containerfile.") ||
        this == "makefile" ||
        startsWith("makefile.") ||
        this == "readme" ||
        this == "license" ||
        this == "licence" ||
        this == "copying" ||
        this == "notice" ||
        this == "gemfile" ||
        this == "rakefile"
